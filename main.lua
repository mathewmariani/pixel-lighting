	vec3 light_color = vec3(1.0, 1.0, 1.0);

	vec4 effect(vec4 color, Image texture, vec2 texture_coords, vec2 pixel_coords) {
		vec4 texcolor = Texel(texture, texture_coords);
		vec3 occlusion_map = Texel(occlusion_map, texture_coords).rgb;
		float bypass = occlusion_map.g;

		// normal
		vec3 normal  = Texel(normal_map, texture_coords).rgb;
		normal = normalize(normal * 2.0 - 1.0);

		// light values
		vec3 light_direction = light_position - vec3(pixel_coords, 0);
		float light_distance = length(light_direction);
		float attenuation = smoothstep(100, 50, light_distance);
		light_direction = normalize(light_direction);
		
		// ambient
		vec3 ambient = (0.1 + occlusion_map.r * 0.6) * light_color;

		// diffuse
		vec3 diffuse = clamp(dot(normal, light_direction), 0.0, 1.0) * light_color;

		// specular
		vec3 halfway= normalize(light_direction);  
		vec3 specular = pow(max(dot(normal, halfway), 0.0), 256.0) * light_color;
		specular *= 0.05;

		// attenuation
		ambient *= attenuation;
		diffuse *= attenuation;
		specular *= attenuation;

		// light
		vec3 light = (ambient + diffuse + specular);

		// cel shading
		light *= floor(light * 2) * 0.5;

		// final result
		return vec4(light * texcolor.rgb, texcolor.a);
	}
]]
 
 -- vertex shader
local vertexcode = [[
	vec4 position(mat4 transform_projection, vec4 vertex_position) {
		return transform_projection * vertex_position;
	}
]]

function love.load()
	local function load_image(file, name, flags)
		local decoded = love.data.decode("string", "base64", file)
		return love.graphics.newImage(love.filesystem.newFileData(decoded, name:gsub("_", ".")), flags)
	end

	-- load base64 images
	images = {}

	-- chair
	images.chair = {}
	images.chair.albedo = load_image(chair_albedo_png, "chair_albedo.png")
	images.chair.normal = load_image(chair_normal_png, "chair_normal.png")
	images.chair.occlusion = load_image(chair_occlusion_png, "chair_occlusion.png")

	-- pithos
	images.pithos = {}
	images.pithos.albedo = load_image(pithos_albedo_png, "pithos_albedo.png")
	images.pithos.normal = load_image(pithos_normal_png, "pithos_normal.png")
	images.pithos.occlusion = load_image(pithos_occlusion_png, "pithos_occlusion.png")
	
	-- globe
	images.globe = load_image(globe_png, "globe.png")

	-- create shader
	shader = love.graphics.newShader(pixelcode, vertexcode)

	-- default
	draw = {}
	draw.albedo = images.chair.albedo
	draw.normal = images.chair.normal
	draw.occlusion = images.chair.occlusion
end

local toggle = true
function love.keypressed(key, scancode, isrepeat)
	if key == "tab" then toggle = not toggle end
end

-- values for light position
local angle = 0
local radius = 80
local x, y

function love.update(dt)
	-- rotate globe around center
	x = cx + radius * math.cos(angle)
	y = cy + radius * math.sin(angle)
	angle = angle + math.pi * dt * 0.5

	if toggle then
		draw.albedo = images.chair.albedo
		draw.normal = images.chair.normal
		draw.occlusion = images.chair.occlusion
	else
		draw.albedo = images.pithos.albedo
		draw.normal = images.pithos.normal
		draw.occlusion = images.pithos.occlusion
	end
end

function love.draw()
	-- render to canvas
	canvas:renderTo(function()
		
		-- clear canvas
		love.graphics.clear()
		
		-- bind shader
		love.graphics.setShader(shader)
		shader:send("light_position", { x / 2, y / 2, 20.0 })

		shader:send("normal_map", draw.normal)
		shader:send("occlusion_map", draw.occlusion)

		-- draw chair
		love.graphics.draw(draw.albedo,
			(cx/2) - (draw.albedo:getWidth() / 2),
			(cy/2) - (draw.albedo:getHeight() / 2)
		)

		-- unbind shader
		love.graphics.setShader()

   		-- draw light source (offset globe by 9px)
		love.graphics.draw(images.globe, (x / 2), (y / 2), 0, 1, 1, 9, 9)
	end)
	
	-- draw canvas
	love.graphics.draw(canvas, 0, 0, 0, 2, 2)
end